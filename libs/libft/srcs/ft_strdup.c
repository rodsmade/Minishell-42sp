#include "libft.h"

char	*ft_strdup(const char *s)
{
	unsigned int	strlength;
	char			*newstr;

	if (!s)
		return (NULL);
	strlength = ft_strlen(s);
	newstr = ft_calloc((strlength + 1), sizeof(const char));
	if (newstr == NULL)
		return (NULL);
	newstr = ft_memcpy(newstr, s, strlength);
	return (newstr);
}
